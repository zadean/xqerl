-include_lib("common_test/include/ct.hrl").
-export([all/0,
         suite/0]).
-export([init_per_suite/1,
         end_per_suite/1]).
-export(['math-asin-001'/1]).
-export(['math-asin-002'/1]).
-export(['math-asin-003'/1]).
